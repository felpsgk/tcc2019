    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        DAO<ObjConhecimento> dao = new DAO<>();

        ObjConhecimento o = new ObjConhecimento();
        Curso curso = null;
        if (cboxCurso.getSelectedIndex() != 0) {
            curso = (Curso) cboxCurso.getSelectedItem();
            o.setCurso(curso);
        }

        if (!txtDesc.getText().isEmpty()) {
            o.setDescricao(txtDesc.getText());
        }

        try {
            if (lblCurso.getText().isEmpty()) {
                dao.save(o);
            } else {
                ObjConhecimento oc = (ObjConhecimento) cboxEdit.getSelectedItem();

                oc.setDescricao(txtDesc.getText());
                if (curso != null) {
                    oc.setCurso(curso);
                } else {
                    oc.setCurso(oc.getCurso());
                }
                dao.update(oc);
            }
        } catch (Exception ex) {
            System.err.println("Erro ao salvar capacidade: " + ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void cboxEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboxEditActionPerformed
        ObjConhecimento oc = (ObjConhecimento) cboxEdit.getSelectedItem();
        if (oc != null) {
            txtDesc.setText(oc.getDescricao());
            lblCurso.setText(oc.getCurso().getNome());
        }
    }//GEN-LAST:event_cboxEditActionPerformed


