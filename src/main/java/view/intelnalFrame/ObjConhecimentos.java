    private void popCbox(Integer index) {

        switch (index) {
            case 1:
                try {
                    ObjConhecimentoDAO dao = new ObjConhecimentoDAO();
                    dao.findAll().forEach((s) -> {
                        cboxEdit.addItem(s);
                    });
                } catch (Exception e) {
                    System.out.println("Erro ao popular cbox 1: " + e);
                }
                break;
            case 2:
                try {
                    CursoDAO dao = new CursoDAO();
                    dao.findAll().forEach((s) -> {
                        cboxCurso.addItem(s);
                    });
                } catch (Exception e) {
                    System.out.println("Erro ao popular cbox 2:" + e);
                }
                break;

        }

    }

